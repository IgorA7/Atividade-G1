const num1 = 8;
const num2 = 18;

function Operacoes() {
    resultado = num1 + num2;
    console.log('O resultado da soma de '+ num1 + ' + ' + num2 + 'é: ' + resultado);
    console.log('--//--');
    resultado = num1 - num2;
    console.log('O resultado da subtração de '+ num1 + ' + ' + num2 + ' é: ' + resultado);
    console.log('--//--');
    resultado = num1 / num2;
    console.log('O resultado da divisão de '+ num1 + ' + ' + num2 + ' é: ' + resultado);
    console.log('--//--');
    resultado = num1 * num2;
    console.log('O resultado da multiplicação de '+ num1 + ' + ' + num2 + ' é: ' + resultado);
    console.log('--//--');
    resultado = num1 % num2;
    console.log('O resto da divisão de '+ num1 + ' + ' + num2 + ' é: ' + resultado);
    console.log('--//--');
}

Operacoes();